/**
 * P365 OneNote Add-in — Notebook Plug-in
 *
 * Inserts P365 calculation results into OneNote notebook pages for
 * field engineers, O&M teams, and project documentation.
 *
 * Planned Features:
 *   - Insert P365 calculation block into a OneNote page (table + results)
 *   - Create a "P365 Job Log" notebook section automatically
 *   - Pipe sizing result block (with inline unit conversions)
 *   - Well performance snapshot (rate, pressure, skin)
 *   - Gas composition analysis block
 *   - PTA interpretation notes (permeability, skin, PI)
 *   - Field measurement log (date/time-stamped entries)
 *
 * Implementation Plan:
 *   1. Office.js OneNote API task pane (React)
 *   2. OneNote Page HTML content insertion (insertHtmlContent)
 *   3. Structured note blocks with P365 results as HTML tables
 *   4. Auto-create sections: "Pipe Sizing", "Well Tests", "PVT Data"
 *   5. Cross-reference with Excel workbook (via Office shared runtime)
 *
 * Architecture:
 *   src/addins/onenote/
 *     taskpane.html          — OneNote task pane entry
 *     taskpane.tsx           — React: note block builder
 *     blocks/
 *       pipe-sizing-block.ts — HTML block for pipe sizing notes
 *       well-log-block.ts    — HTML block for well test notes
 *       pvt-block.ts         — HTML block for PVT data
 *       composition-block.ts — HTML block for gas composition
 *     commands.ts            — Ribbon command handlers
 *
 * Office API Requirements:
 *   @microsoft/office-js >= 1.1
 *   OneNote API 1.1+
 *   Permission: ReadWriteDocument
 *
 * Manifest entry (onenote-manifest.xml):
 *   <Host Name="Notebook" />
 */

