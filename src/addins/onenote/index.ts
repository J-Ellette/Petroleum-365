/**
 * Build OneNote HTML for a calculation note block.
 * OneNote page HTML is inserted via the Page.insertHtmlContent API.
 *
 * @param title       Block title (e.g., "Pipe Sizing — Job #12345")
 * @param rows        Key-value results
 * @param timestamp   ISO timestamp string
 * @param notes       Optional engineer notes
 * @returns           HTML string suitable for OneNote page insertion
 */
export function buildOneNoteBlock(
  title: string,
  rows: Record<string, string>,
  timestamp: string,
  notes?: string,
): string {
  const rowHtml = Object.entries(rows)
    .map(([k, v]) => `<tr><td><b>${k}</b></td><td>${v}</td></tr>`)
    .join("");

  const notesHtml = notes
    ? `<p><b>Notes:</b> ${notes}</p>`
    : "";

  return `
<div>
  <h2>${title}</h2>
  <p><i>Generated by Petroleum 365 (P365) — ${timestamp}</i></p>
  <table border="1" style="border-collapse:collapse;font-family:Calibri,sans-serif">
    <tr>
      <th style="background:#1a5276;color:white;padding:4px 8px">Parameter</th>
      <th style="background:#1a5276;color:white;padding:4px 8px">Value</th>
    </tr>
    ${rowHtml}
  </table>
  ${notesHtml}
</div>`.trim();
}

/**
 * Build a field measurement log entry for OneNote.
 *
 * @param wellName    Well or location name
 * @param timestamp   Measurement timestamp (ISO string)
 * @param measurements  Measured values
 * @param engineer    Engineer's name
 * @returns           HTML suitable for OneNote page insertion
 */
export function buildFieldMeasurementLog(
  wellName: string,
  timestamp: string,
  measurements: Array<{ parameter: string; value: number; unit: string }>,
  engineer: string,
): string {
  const rowHtml = measurements
    .map(m => `<tr><td>${m.parameter}</td><td>${m.value} ${m.unit}</td></tr>`)
    .join("");

  return `
<div>
  <h3>📋 Field Measurement Log — ${wellName}</h3>
  <p><b>Date/Time:</b> ${timestamp} | <b>Recorded by:</b> ${engineer}</p>
  <table border="1" style="border-collapse:collapse;font-family:Calibri,sans-serif;min-width:300px">
    <tr>
      <th style="background:#2e86c1;color:white;padding:4px 8px">Parameter</th>
      <th style="background:#2e86c1;color:white;padding:4px 8px">Measured Value</th>
    </tr>
    ${rowHtml}
  </table>
  <p><i>Recorded with Petroleum 365 (P365)</i></p>
</div>`.trim();
}

